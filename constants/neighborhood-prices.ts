export const getNeighborhoodPrices = (
  neighborhoodId: string,
  propertyType?: PropertyType,
  transactionType?: TransactionType
): NeighborhoodPrice[] => {
  return NEIGHBORHOOD_PRICES.filter((price) => {
    if (price.neighborhoodId !== neighborhoodId) return false;
    if (propertyType && price.propertyType !== propertyType) return false;
    if (transactionType && price.transactionType !== transactionType) return false;
    return true;
  });
};

export const getAvgPriceByNeighborhood = (
  neighborhoodId: string,
  propertyType: PropertyType,
  transactionType: TransactionType
): number | null => {
  const prices = getNeighborhoodPrices(neighborhoodId, propertyType, transactionType);
  if (prices.length === 0) return null;
  return prices[0].avgPrice;
};
